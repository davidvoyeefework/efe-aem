package com.efe.core.models.bean;

/**
 * The Class Podcast.
 *
 */
public class Podcast {
	
	/** The id. */
	private String id;
	
	/** The title. */
	private String title;
	
	/** The description html. */
	private String descriptionHtml;
	
	/** The season. */
	private int season;
	
	/** The episode. */
	private int episode;
	
	/** The season episode label. */
	private String seasonEpisodeLabel;
	
	/** The page link. */
	private String pageLink;
	
	/** The embed url. */
	private String embedUrl;
	
	/**
	 * Gets the title.
	 *
	 * @return the title
	 */
	public String getTitle() {
		return title;
	}

	/**
	 * Sets the title.
	 *
	 * @param title the title to set
	 */
	public void setTitle(String title) {
		this.title = title;
	}

	/**
	 * Gets the description html.
	 *
	 * @return the descriptionHtml
	 */
	public String getDescriptionHtml() {
		return descriptionHtml;
	}

	/**
	 * Sets the description html.
	 *
	 * @param descriptionHtml the descriptionHtml to set
	 */
	public void setDescriptionHtml(String descriptionHtml) {
		this.descriptionHtml = descriptionHtml;
	}

	/**
	 * Gets the season.
	 *
	 * @return the season
	 */
	public int getSeason() {
		return season;
	}

	/**
	 * Sets the season.
	 *
	 * @param season the season to set
	 */
	public void setSeason(int season) {
		this.season = season;
	}

	/**
	 * Gets the episode.
	 *
	 * @return the episode
	 */
	public int getEpisode() {
		return episode;
	}

	/**
	 * Sets the episode.
	 *
	 * @param episode the episode to set
	 */
	public void setEpisode(int episode) {
		this.episode = episode;
	}

	/**
	 * Gets the season episode label.
	 *
	 * @return the seasonEpisodeLabel
	 */
	public String getSeasonEpisodeLabel() {
		return seasonEpisodeLabel;
	}

	/**
	 * Sets the season episode label.
	 *
	 * @param seasonEpisodeLabel the seasonEpisodeLabel to set
	 */
	public void setSeasonEpisodeLabel(String seasonEpisodeLabel) {
		this.seasonEpisodeLabel = seasonEpisodeLabel;
	}

	/**
	 * Gets the page link.
	 *
	 * @return the pageLink
	 */
	public String getPageLink() {
		return pageLink;
	}

	/**
	 * Sets the page link.
	 *
	 * @param pageLink the pageLink to set
	 */
	public void setPageLink(String pageLink) {
		this.pageLink = pageLink;
	}

	/**
	 * Gets the id.
	 *
	 * @return the id
	 */
	public String getId() {
		return id;
	}

	/**
	 * Sets the id.
	 *
	 * @param id the id to set
	 */
	public void setId(String id) {
		this.id = id;
	}
	
	/**
	 * Gets the embed url.
	 *
	 * @return the embedUrl
	 */
	public String getEmbedUrl() {
		return embedUrl;
	}

	/**
	 * Sets the embed url.
	 *
	 * @param embedUrl the embedUrl to set
	 */
	public void setEmbedUrl(String embedUrl) {
		this.embedUrl = embedUrl;
	}


}

package com.efe.core.models;

/**
 * The Interface ModalPromotion Model.
 */
public interface ModalPromotion {

    /**
     * Gets the Pre-title.
     *
     * @return the Pre-title
     */
    String getPretitle();

    /**
     * Gets the Title.
     *
     * @return the Title
     */
    String getTitle();

    /**
     * Gets the Description.
     *
     * @return the Description
     */
    String getDescription();

    /**
     * Gets the Button Text.
     *
     * @return the Button Text
     */
    String getButtonText();

    /**
     * Gets the Button Url.
     *
     * @return the Button Url
     */
    String getButtonUrl();

    /**
     * Gets the Promotion Image.
     *
     * @return the Promotion Image
     */
    String getPromotionImage();

    /**
     * Gets the Promotion Image Alt Text.
     *
     * @return the Promotion Image Alt Text
     */
    String getPromotionImageAltText();

    /**
     * Gets the Close Button Color.
     *
     * @return the Close Button Color
     */
    String getCloseButtonColor();

    /**
     * Gets the Close Button Alt Text.
     *
     * @return the Close Button Alt Text
     */
    String getCloseButtonAltText();

    /**
     * Gets the Start Date.
     *
     * @return the Start Date
     */
    String getStartDate();

    /**
     * Gets the End Date.
     *
     * @return the End Date
     */
    String getEndDate();

    /**
     * Gets the id.
     *
     * @return the id
     */
    String getId();

}

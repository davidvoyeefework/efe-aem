package com.efe.core.models;

/**
 * The Interface Forms.
 */
public interface FormsSelector {

	/**
	 * Gets the form id.
	 *
	 * @return the form id
	 */
	String getFormId();

	/**
	 * Gets the id.
	 *
	 * @return the id
	 */
	String getId();

	/**
	 * Gets the form url.
	 *
	 * @return the form url
	 */
	String getFormUrl();

	/**
	 * Gets the form js url.
	 *
	 * @return the form js url
	 */
	String getFormJsUrl();

}
